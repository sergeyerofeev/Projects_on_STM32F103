/**
 * @brief This function handles USART1 global interrupt.
 */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  if ((UART1.Instance->SR & USART_SR_RXNE) != RESET) {
    uint8_t rbyte = (uint8_t) (UART1.Instance->DR & (uint8_t) 0x00FF);
    uint16_t i = (uint16_t) (rx1_buffer_head + 1) % UART1_RX_BUFFER_SIZE;

    if (i != rx1_buffer_tail) {
      rx1_buffer[rx1_buffer_head] = rbyte;
      rx1_buffer_head = i;
    }
  }
  return;
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

